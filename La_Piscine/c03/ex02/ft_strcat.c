char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	src_i;

	i = 0;
	src_i = 0;
	while (dest[i] != '\0')
		i++;
	while (src[src_i] != '\0')
	{
		dest[i] = src[src_i];
		i++;
		src_i++;
	}
	dest[i] = '\0';
	return (dest);
}
