void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	k;

	i = size - 1;
	j = 0;
	while (i > 0)
	{
		j = 0;
		while (j < i)
		{
			if (tab[j] > tab[j + 1])
			{
				k = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = k;
			}
			j++;
		}
		i--;
	}
}
