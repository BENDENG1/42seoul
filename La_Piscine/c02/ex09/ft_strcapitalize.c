char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			str[i] += 32;
		if (i == 0)
			if ('a' <= str[i] && str[i] <= 'z')
				str[i] -= 32;
		if (i > 0)
		{
			if (!(('A' <= str[i - 1] && str[i - 1] <= 'Z')
					|| ('a' <= str[i - 1] && str[i - 1] <= 'z')
					|| ('0' <= str[i - 1] && str[i - 1] <= '9')))
				if ('a' <= str[i] && str[i] <= 'z')
					str[i] -= 32;
		}
		i++;
	}
	return (str);
}
