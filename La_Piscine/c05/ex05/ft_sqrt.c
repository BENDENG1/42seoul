int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	else
	{
		while (i <= 46340 && i < nb)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
		return (0);
	}
}
