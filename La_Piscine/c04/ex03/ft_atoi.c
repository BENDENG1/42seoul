int	ft_atoi(char *str)
{
	int	i;
	int	pm;
	int	ans;

	ans = 0;
	i = 0;
	pm = 1;
	if (str[i] != '\0')
	{
		while ((9 <= str[i] && str[i] <= 13) || str[i] == 32)
			i++;
		while (str[i] == '-' || str[i] == '+')
		{
			if (str[i] == '-')
				pm *= -1;
			i++;
		}
		while ('0' <= str[i] && str[i] <= '9')
		{
			ans *= 10;
			ans += (str[i] - '0');
			i++;
		}
	}
	return (ans * pm);
}
