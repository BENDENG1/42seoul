#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	s_length;
	size_t	i;

	s_length = 0;
	i = 0;
	while (src[s_length] != '\0')
		s_length++;
	while (src[i] != '\0' && i + 1 < size)
	{
		dest[i] = src[i];
		i++;
	}
	if (size > 0)
		dest[i] = '\0';
	return (s_length);
}
